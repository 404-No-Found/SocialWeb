package javabean;

public class Mid {
    int id;
    String releaser;
    String responser;
    int dataId;
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRelease() {
        return releaser;
    }

    public void setRelease(String releaser) {
        this.releaser = releaser;
    }

    public String getResponser() {
        return responser;
    }

    public void setResponser(String responser) {
        this.responser = responser;
    }

    public int getDataId() {
        return dataId;
    }

    public void setDataId(int dataId) {
        this.dataId = dataId;
    }


}
